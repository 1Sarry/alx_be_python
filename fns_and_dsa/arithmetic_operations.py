def perform_operation(num1, num2, operation):


   if operation == "divide":
      result= num1/num2
      return result

   elif operation == "multiply":
      result = num1*num2
      return result
   elif operation == "subtract":
      result = num2- num1
      return result
   elif operation == "add":
      result = num1 + num2
      return result

